def build_dp_cherry_pickup():
    slug = "dp-cherry-pickup"

    def solve(inp: str) -> str:
        toks = inp.split()
        idx = 0
        n = int(toks[idx]); idx += 1
        m = int(toks[idx]); idx += 1
        g = []
        for i in range(n):
            row = list(map(int, toks[idx:idx + m]))
            idx += m
            g.append(row)
        NEG = -10 ** 9
        # dp[k][i1][i2] = best after k moves; positions (i1, k-i1) and (i2, k-i2)
        dp = [[[NEG] * n for _ in range(n)] for __ in range(n + m - 1)]
        dp[0][0][0] = g[0][0]
        for k in range(1, n + m - 1):
            for i1 in range(max(0, k - (m - 1)), min(n - 1, k) + 1):
                j1 = k - i1
                if not (0 <= j1 < m):
                    continue
                for i2 in range(max(0, k - (m - 1)), min(n - 1, k) + 1):
                    j2 = k - i2
                    if not (0 <= j2 < m):
                        continue
                    best = NEG
                    for di1 in (0, -1):
                        pi1 = i1 + di1
                        for di2 in (0, -1):
                            pi2 = i2 + di2
                            if pi1 < 0 or pi2 < 0:
                                continue
                            v = dp[k - 1][pi1][pi2]
                            if v > best:
                                best = v
                    if best == NEG:
                        continue
                    if i1 == i2:
                        dp[k][i1][i2] = best + g[i1][j1]
                    else:
                        dp[k][i1][i2] = best + g[i1][j1] + g[i2][j2]
        ans = dp[n + m - 2][n - 1][n - 1]
        return str(max(ans, 0))

