def build_dp_russian_doll():
    slug = "dp-russian-doll"

    def solve(inp: str) -> str:
        toks = inp.split()
        n = int(toks[0])
        items = []
        for i in range(n):
            w = int(toks[1 + 2 * i])
            h = int(toks[2 + 2 * i])
            items.append((w, h))
        # sort by w asc, for equal w sort by h desc (to ensure LIS won't pick same w)
        items.sort(key=lambda x: (x[0], -x[1]))
        # LIS on h
        from bisect import bisect_left
        tails = []
        for _, h in items:
            pos = bisect_left(tails, h)
            if pos == len(tails):
                tails.append(h)
            else:
                tails[pos] = h
        return str(len(tails))

