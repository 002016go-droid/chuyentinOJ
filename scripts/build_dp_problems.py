        children = [[] for _ in range(n + 1)]
        for i in range(1, n):
            parent[i + 1] = int(toks[idx]); idx += 1
            children[parent[i + 1]].append(i + 1)
        # Root is 1; we treat virtual root 0 children = [1] so we pick at most m courses total
        children[0].append(1)
        # f[v][k] = max stars picking exactly k courses in subtree(v), MUST take v if v != 0
        INF = -1
        def dfs(v):
            if v == 0:
                # virtual root: no cost itself
                f = [0] + [INF] * m
            else:
                if h[v - 1] > m:
                    return [0] + [INF] * m
                f = [INF] * (m + 1)
                f[0] = 0  # don't take v's subtree at all
                f[h[v - 1]] = s[v - 1]  # take v alone
            for c in children[v]:
                fc = dfs(c)
                new_f = [INF] * (m + 1)
                for k in range(m + 1):
                    if f[k] == INF:
                        continue
                    for kk in range(m - k + 1):
                        if fc[kk] == INF:
                            continue
                        cand = f[k] + fc[kk]
                        if cand > new_f[k + kk]:
                            new_f[k + kk] = cand
                f = new_f
            return f

        # avoid Python recursion-limit on deeper trees: do iterative DFS using post-order
        # but with n ≤ a couple hundred recursion is fine.
        sys.setrecursionlimit(10000)
        f = dfs(0)
        ans = max(x for x in f if x != INF)
        return str(ans)

