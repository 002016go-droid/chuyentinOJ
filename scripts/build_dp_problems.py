        out = []
        while i > 0 and j > 0:
            if a[i - 1] == b[j - 1]:
                out.append(a[i - 1])
                i -= 1; j -= 1
            elif dp[i - 1][j] >= dp[i][j - 1]:
                i -= 1
            else:
                j -= 1
