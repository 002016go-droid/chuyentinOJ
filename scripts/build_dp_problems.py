def build_tdp_min_vcover():
    slug = "tdp-min-vcover"

    def solve(inp: str) -> str:
        toks = inp.split()
        idx = 0
        n = int(toks[idx]); idx += 1
        adj = [[] for _ in range(n + 1)]
        for _ in range(n - 1):
            u = int(toks[idx]); idx += 1
            v = int(toks[idx]); idx += 1
            adj[u].append(v)
            adj[v].append(u)
        # iterative DFS rooted at 1
        # dp[v][0] = min VC subtree v without taking v
        # dp[v][1] = min VC subtree v taking v
        order = []
        parent = [0] * (n + 1)
        stack = [1]
        seen = [False] * (n + 1)
        seen[1] = True
        while stack:
            u = stack.pop()
            order.append(u)
            for w in adj[u]:
                if not seen[w]:
                    seen[w] = True
                    parent[w] = u
                    stack.append(w)
        dp0 = [0] * (n + 1)
        dp1 = [1] * (n + 1)
        for u in reversed(order):
            for w in adj[u]:
                if parent[w] == u:
                    dp0[u] += dp1[w]            # must take child
                    dp1[u] += min(dp0[w], dp1[w])
        return str(min(dp0[1], dp1[1]))

