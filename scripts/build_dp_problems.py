    def solve(inp: str) -> str:
        toks = inp.split()
        idx = 0
        n = int(toks[idx]); idx += 1
        adj = [[] for _ in range(n + 1)]
        for _ in range(n - 1):
            u = int(toks[idx]); idx += 1
            v = int(toks[idx]); idx += 1
            w = int(toks[idx]); idx += 1
            adj[u].append((v, w))
            adj[v].append((u, w))
        order = []
        parent = [0] * (n + 1)
        parent_w = [0] * (n + 1)
        seen = [False] * (n + 1)
        stack = [1]
        seen[1] = True
        while stack:
            u = stack.pop()
            order.append(u)
            for v, w in adj[u]:
                if not seen[v]:
                    seen[v] = True
                    parent[v] = u
                    parent_w[v] = w
                    stack.append(v)
