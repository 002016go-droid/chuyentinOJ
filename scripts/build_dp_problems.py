def patch_roadmap() -> None:
    """Append (slug+metadata) into roadmap.json node.problems[] (preserving existing)."""
    data = json.loads(ROADMAP.read_text(encoding="utf-8"))
    touched = 0
    for node in data["nodes"]:
        topic = node["id"]
        if topic not in NEW_BY_TOPIC:
            continue
        # Preserve existing problem entries as-is. Existing are dicts on disk.
        existing_slugs = set()
        for p in node.get("problems", []):
            slug = p["slug"] if isinstance(p, dict) else p
            existing_slugs.add(slug)
        added = 0
        for (slug, title, tier, skills) in NEW_BY_TOPIC[topic]:
            if slug in existing_slugs:
                continue
            node["problems"].append({
                "slug": slug,
                "title": title,
                "tier": tier,
                "skills": skills,
                "difficulty": "intermediate",
                "maxStars": 3,
                "tags": PROBLEM_TAGS.get(slug, ["dp"]),
            })
            added += 1
        if added:
            touched += 1
            print(f"[roadmap] {topic}: +{added} problems")
    ROADMAP.write_text(
        json.dumps(data, indent=2, ensure_ascii=False) + "\n",
        encoding="utf-8",
    )
    print(f"[roadmap] patched {touched} nodes")


def rebuild_index() -> None:
    slugs = sorted(p.stem for p in PROBLEMS_DIR.glob("*.json") if p.name != "index.json")
    INDEX.write_text(
        json.dumps(slugs, indent=2, ensure_ascii=False) + "\n",
        encoding="utf-8",
    )
    print(f"[index] wrote {len(slugs)} slugs to {INDEX.relative_to(ROOT)}")


