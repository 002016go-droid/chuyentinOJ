    hints = [
        {"id": f"{slug}-h1", "subtaskId": "st1", "content": "Vét cạn tất cả đoạn O(N²) hoặc O(N³).", "starCost": 0},
        {"id": f"{slug}-h2", "subtaskId": "st2", "content": "Kadane biến thể: số âm khi nhân số âm thành dương, nên giữ cả max và min.", "starCost": 1},
        {"id": f"{slug}-h3", "subtaskId": "st3", "content": "Duyệt 1 lần O(N), cập nhật cur_max/cur_min.", "starCost": 1},
    ]
    return base_problem(
        slug=slug, title="Tích lớn nhất của dãy con liên tiếp",
        title_en="Maximum Product Subarray",
        topic_id="dp-intro", tier="T2",
        skills=["S2-Phân tích", "S4-Cài đặt"], difficulty=2,
        tags=["dp", "dp-intro", "kadane"], statement=statement,
        vn_pattern="Kadane biến thể; vì có số âm phải giữ cả min lẫn max.",
        subtasks=subtasks, samples=samples, test_cases=cases, hints=hints,
        editorial="Duyệt mảng, giữ `cur_max`, `cur_min` là max/min của tích các đoạn kết thúc tại i. Khi gặp số âm, tích tối thiểu có thể trở thành tối đa, nên ta cần cả hai. Công thức: cur_max = max(x, cur_max*x, cur_min*x), cur_min tương tự với min. Đáp án = max cur_max. O(N).",
        sketch="// cur_max,cur_min; ans=max(ans,cur_max); update both with max/min of {x, cur_max*x, cur_min*x}",
    )


