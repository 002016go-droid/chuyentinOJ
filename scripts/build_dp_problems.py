def build_dp_lcs_print():
    slug = "dp-lcs-print"

    def solve(inp: str) -> str:
        toks = inp.split("\n")
        a = toks[0].strip()
        b = toks[1].strip() if len(toks) > 1 else ""
        n, m = len(a), len(b)
        dp = [[0] * (m + 1) for _ in range(n + 1)]
        for i in range(1, n + 1):
            for j in range(1, m + 1):
                if a[i - 1] == b[j - 1]:
                    dp[i][j] = dp[i - 1][j - 1] + 1
                else:
                    dp[i][j] = max(dp[i - 1][j], dp[i][j - 1])
