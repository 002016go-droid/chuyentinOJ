def write_problem(problem: dict):
    """Validate basic invariants, then dump to public/data/problems/<slug>.json."""
    slug = problem["slug"]
    # Sanity: every testCase points to a real subtaskId, sub points add up.
    subtask_ids = {st["id"] for st in problem["subtasks"]}
    for tc in problem["testCases"]:
        assert tc["subtaskId"] in subtask_ids, f"{slug}: tc {tc['id']} has unknown sub {tc['subtaskId']}"
    pts = sum(st["points"] for st in problem["subtasks"])
    assert pts == 100, f"{slug}: subtask points sum to {pts}, expected 100"
    out = PROBLEMS_DIR / f"{slug}.json"
    out.write_text(json.dumps(problem, indent=2, ensure_ascii=False) + "\n", encoding="utf-8")


