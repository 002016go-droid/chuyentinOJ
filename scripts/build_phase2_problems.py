    def solve(inp):
        n, adj, _, _ = _read_tree(inp)
        if n == 1:
            return "0"
        order, parent = _iter_dfs_order(n, adj)
        depth = [0] * (n + 1)
        best = [0] * (n + 1)  # longest path going down from u
        ans = 0
        for u in reversed(order):
            top1 = 0
            top2 = 0
            for c in adj[u]:
                if c == parent[u]:
                    continue
                d = best[c] + 1
                if d > top1:
                    top2 = top1
                    top1 = d
                elif d > top2:
                    top2 = d
            best[u] = top1
            if top1 + top2 > ans:
                ans = top1 + top2
        return str(ans)

