    def solve(inp):
        toks = inp.split()
        n = int(toks[0])
        a = [0] + [int(x) for x in toks[1:1 + n]]
        adj = [[] for _ in range(n + 1)]
        idx = 1 + n
        for _ in range(n - 1):
            u = int(toks[idx]); v = int(toks[idx + 1]); idx += 2
            adj[u].append(v); adj[v].append(u)
