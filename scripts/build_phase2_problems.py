def _gen_tree(rng, n):
    """Random tree on N nodes; return list of (u, v) edges (1-indexed)."""
    edges = []
    for v in range(2, n + 1):
        u = rng.randint(1, v - 1)
        edges.append((u, v))
    return edges


def _read_tree(inp):
    toks = inp.split()
    n = int(toks[0])
    adj = [[] for _ in range(n + 1)]
    idx = 1
    for _ in range(n - 1):
        u = int(toks[idx]); v = int(toks[idx + 1]); idx += 2
        adj[u].append(v)
        adj[v].append(u)
    return n, adj, toks, idx


def _iter_dfs_order(n, adj, root=1):
    """Return BFS order + parent array (iterative, safe for big N)."""
    order = [root]
    parent = [0] * (n + 1)
    parent[root] = -1
    head = 0
    while head < len(order):
        u = order[head]; head += 1
        for v in adj[u]:
            if v != parent[u]:
                parent[v] = u
                order.append(v)
    return order, parent


