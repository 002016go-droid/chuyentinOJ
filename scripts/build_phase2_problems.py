def generate_cases(slug: str, subtasks: list, solver: Callable[[str], str]) -> tuple:
    """For each subtask, generate ~5 test inputs by calling subtask['gen'](rng).
    Returns (test_cases, samples) where samples = first 2 cases from sub1.
    """
    rng = random.Random(hash(slug) & 0xFFFFFFFF)
    cases = []
    samples = []
    tc_id = 0
    for st in subtasks:
        for k in range(st["nCases"]):
            tc_id += 1
            inp = st["gen"](rng)
            out = solver(inp)
            cases.append({
                "id": f"tc{tc_id:02d}",
                "subtaskId": st["id"],
                "input": inp,
                "output": out,
            })
            if st["id"] == subtasks[0]["id"] and len(samples) < 2:
                samples.append({
                    "id": f"sample{len(samples) + 1}",
                    "input": inp,
                    "output": out,
                })
    return cases, samples


