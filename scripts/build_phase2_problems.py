        n = int(toks[0])
        adj = [[] for _ in range(n + 1)]
        for i in range(2, n + 1):
            b = int(toks[i - 2 + 1])  # toks[1] = boss of 2
            adj[b].append(i)
        # Iterative count of descendants
        cnt = [0] * (n + 1)
        order = [1]
        head = 0
