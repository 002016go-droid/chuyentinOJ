# sf-prime-table: đếm số nguyên tố trong [L, R]
# ---------------------------------------------------------------------------
def build_sf_prime_table():
    slug = "sf-prime-table"
    MAX = 200_000

    def solve(inp: str) -> str:
        toks = inp.split()
        q = int(toks[0])
        out = []
        for i in range(q):
            l = int(toks[1 + 2 * i])
            r = int(toks[2 + 2 * i])
            # naive: trial division for each n in [l, r]
            cnt = 0
            for n in range(l, r + 1):
                if n < 2:
                    continue
                ok = True
                d = 2
                while d * d <= n:
                    if n % d == 0:
                        ok = False
                        break
                    d += 1
                if ok:
                    cnt += 1
            out.append(str(cnt))
        return "\n".join(out)

    def g1(rng):
        # sub1: small numbers, R ≤ 100
        q = rng.randint(1, 5)
        lines = [str(q)]
        for _ in range(q):
            l = rng.randint(1, 50)
            r = rng.randint(l, l + 50)
            lines.append(f"{l} {r}")
        return "\n".join(lines) + "\n"

    def g2(rng):
        # sub2: medium, R ≤ 5000
        q = rng.randint(2, 8)
        lines = [str(q)]
        for _ in range(q):
            l = rng.randint(1, 4000)
            r = rng.randint(l, min(l + 2000, 5000))
            lines.append(f"{l} {r}")
        return "\n".join(lines) + "\n"

    def g3(rng):
        # sub3: large, R ≤ 200000 (sieve needed for full marks)
        q = rng.randint(1, 5)
        lines = [str(q)]
        for _ in range(q):
            l = rng.randint(1, MAX - 100)
            r = rng.randint(l, min(l + 200, MAX))
            lines.append(f"{l} {r}")
        return "\n".join(lines) + "\n"

    subtasks = [
        {"id": "st1", "title": "1 ≤ R ≤ 100, Q ≤ 5", "points": 30, "constraint": "1 ≤ L ≤ R ≤ 100; Q ≤ 5", "nCases": 6, "gen": g1},
        {"id": "st2", "title": "1 ≤ R ≤ 5000, Q ≤ 10", "points": 30, "constraint": "1 ≤ L ≤ R ≤ 5000; Q ≤ 10", "nCases": 6, "gen": g2},
        {"id": "st3", "title": "1 ≤ R ≤ 2·10^5, Q ≤ 10 (cần sàng)", "points": 40, "constraint": "1 ≤ L ≤ R ≤ 2·10^5; Q ≤ 10", "nCases": 8, "gen": g3},
    ]
    cases, samples = generate_cases(slug, subtasks, solve)
    for st in subtasks:
        del st["gen"]
        del st["nCases"]
    hints = [
        {"id": f"{slug}-st1", "subtaskId": "st1", "content": "Trial division O(√n) cho mỗi n trong [L, R].", "starCost": 0},
        {"id": f"{slug}-st2", "subtaskId": "st2", "content": "Sàng Eratosthenes lên 5000, sau đó count với prefix sum.", "starCost": 1},
        {"id": f"{slug}-st3", "subtaskId": "st3", "content": "Sàng tới 2·10^5 + prefix `isPrime[]` → mỗi query O(1).", "starCost": 1},
    ]
    statement = (
        "Cho Q truy vấn, mỗi truy vấn là một cặp `(L, R)`. Với mỗi truy vấn, hãy đếm số "
        "số nguyên tố trong đoạn `[L, R]`.\n\n"
        "**Input**: dòng 1 chứa Q; Q dòng tiếp theo, mỗi dòng 2 số L, R.\n\n"
        "**Output**: Q dòng, mỗi dòng 1 số nguyên là đáp án.\n\n"
        "**Ràng buộc**: 1 ≤ L ≤ R ≤ 2·10^5, 1 ≤ Q ≤ 10."
    )
    return base_problem(
        slug=slug, title="Đếm số nguyên tố trong [L, R]", title_en="Count Primes in Range",
        topic_id="sieve-factorization", tier="T2",
        skills=["S4-Cài đặt", "S2-Phân tích"], difficulty=2,
        tags=["sieve", "number-theory"], statement=statement,
        vn_pattern="Sàng Eratosthenes + prefix sum.",
        subtasks=subtasks, samples=samples, test_cases=cases, hints=hints,
        editorial="Tiền xử lý sàng `isPrime[]` rồi prefix `cnt[i] = số nguyên tố ≤ i`. Mỗi query trả `cnt[r] - cnt[l-1]`. O((MAX + Q) log log MAX).",
        sketch="// Sieve + prefix; answer = cnt[r] - cnt[l-1]",
    )


